def get_categories_from_xml(file_name):
    categories = {}
    current_id = ""
    in_categories_section = False

    for event, elem in ET.iterparse(file_name, events=("start", "end")):
        tag = elem.tag.split("}")[-1].lower()

        if event == "start":
            if tag == "categories":
                in_categories_section = True
            elif tag == "category" and in_categories_section:
                current_id = ""
                for attr in elem.attrib:
                    if attr.lower() == "id":
                        current_id = elem.attrib[attr]
                        break

        elif event == "end":
            if tag == "categories":
                in_categories_section = False
            elif tag == "category" and current_id:
                text = (elem.text or "").strip()
                if text:
                    categories[current_id] = text
                current_id = ""
            elem.clear()

    return categories


def parse_products_from_xml(file_name):
    products = []
    in_offer = False
    cur_name = ""
    cur_desc = ""
    cur_cat_ids = []
    cur_tag = ""

    for event, elem in ET.iterparse(file_name, events=("start", "end")):
        tag = elem.tag.split("}")[-1].lower()

        if event == "start":
            if tag in ("offer", "product", "item"):
                in_offer = True
                cur_name = ""
                cur_desc = ""
                cur_cat_ids = []
                cur_tag = ""
            elif in_offer:
                cur_tag = tag

        elif event == "end":
            if in_offer:
                if tag in ("name", "title"):
                    cur_name = (elem.text or "").strip()
                elif tag == "description":
                    cur_desc = (elem.text or "").strip()
                elif tag in ("categoryid", "category_id"):
                    val = (elem.text or "").strip()
                    if val:
                        cur_cat_ids.append(val)

            if tag in ("offer", "product", "item"):
                in_offer = False
                if cur_name:
                    products.append((cur_name, cur_desc, cur_cat_ids))
                cur_tag = ""
                elem.clear()
            else:
                elem.clear()

    return products


def extract_syns_from_desc(desc, name):
    desc_low = desc.lower()

    patterns = [
        r"синоним(?:ы)?(?:\s+к\s+препарату\s+[^\s:]+)?\s*[:.]?\s*([^.]+)",
        r"аналог(?:и)?\s*[:.]?\s*([^.]+)",
        r"заменитель(?:и)?\s*[:.]?\s*([^.]+)",
        r"эквивалент(?:ы)?\s*[:.]?\s*([^.]+)",
    ]

    syn_map = {}
    name_words = [w for w in re.findall(r"[а-яёa-z0-9]+", name.lower()) if len(w) >= 3]
    if not name_words:
        return {}

    for pattern in patterns:
        for m in re.finditer(pattern, desc_low):
            list_str = m.group(1).strip()
            raw_syns = re.split(r"[,;]", list_str)
            for s in raw_syns:
                words = re.findall(r"[а-яёa-z0-9-]+", s.strip())
                if words:
                    phrase = " ".join(words)
                    if len(phrase) >= 3 and phrase not in name_words:
                        if phrase not in syn_map:
                            syn_map[phrase] = []
                        for w in name_words:
                            if w not in syn_map[phrase]:
                                syn_map[phrase].append(w)

    return syn_map


def build_category_synonyms(categories, products):
    cat_to_words = {}

    for prod_name, _desc, cat_ids in products:
        name_words = [w for w in re.findall(r"[а-яёa-z0-9]+", prod_name.lower()) if len(w) >= 3]
        if not name_words:
            continue
        first_word = name_words[0]
        for cat_id in cat_ids:
            cat_name = categories.get(cat_id, "").lower().strip()
            if not cat_name or len(cat_name) < 3:
                continue
            if cat_name not in cat_to_words:
                cat_to_words[cat_name] = []
            if first_word not in cat_to_words[cat_name]:
                cat_to_words[cat_name].append(first_word)

    return {k: v[:25] for k, v in cat_to_words.items()}


